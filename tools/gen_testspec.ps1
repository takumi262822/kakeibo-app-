# Excel に変換
$excelRows = $rows | ForEach-Object {
  $r = $_
  $obj = [ordered]@{}
  for ($i = 0; $i -lt $header.Count; $i++) {
    $obj[$header[$i]] = $r[$i]
  }
  [PSCustomObject]$obj
}

